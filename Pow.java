public class Pow {
    public static void main(String[] args){
        int a = -20;
        int b = 92;
        int c = 638;
        int d = 73;
        int e = 200;
        int f = -22;
        int degree = 2;
        System.out.print(Math.pow(a, degree)+"\t"+Math.pow(b, degree)+"\t"+Math.pow(c, degree)+"\t"+Math.pow(d, degree)+"\t"+Math.pow(e, degree)+"\t"+Math.pow(f, degree));
    }
}
